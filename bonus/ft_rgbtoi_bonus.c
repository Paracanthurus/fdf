#include"fdf_bonus.h"

int	ft_rgbtoi(t_color *rgb)
{
	return ((rgb->t * 0x01000000 + rgb->r * 0x00010000 + rgb->g * 0x00000100
			+ rgb->b * 0x00000001));
}
